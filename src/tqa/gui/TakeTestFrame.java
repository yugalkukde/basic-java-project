    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String choosenOp = getChosenOp();
        if (choosenOp != null) {
            Question question = qstore.getQuestion(pos);
            qno = question.getQno();
            String correctAnswer = question.getCorrectAns();
            Answer newAnswer = new Answer(exam.getExamID(), exam.getLaunguae(), qno, choosenOp, correctAnswer);
            System.out.println(newAnswer);
            Answer answer = astore.getAnswerByQno(qno);
            if (answer == null) {
                astore.addAnswer(newAnswer);
            } else {
                if (newAnswer.getChoosenAnswer().equals(answer.getChoosenAnswer()) == false) {
                    int apos = astore.removeAnswer(answer);
                    astore.setAnswerAt(apos, newAnswer);
                }
            }
        }
        int right = 0, wrong = 0;
        for (Question question1 : qstore.getllQuestion()) {
            int qno = question1.getQno();
            Answer answer = astore.getAnswerByQno(qno);
            if (answer == null) {
                continue;
            } else {
                String chosenAnswer = answer.getChoosenAnswer();
                String correctAnswer = answer.getCorrectAnswer();
                if (chosenAnswer.equals(correctAnswer)) {
                    right++;
                } else if (!chosenAnswer.equals(correctAnswer)) {
                    wrong++;
                }
            }
        }
        JOptionPane.showMessageDialog(null, "Total Question =" + qstore.getCount() + "\n" + "Right =" + right + "\n" + "Wrong =" + wrong + "\n" + "Unattempted =" + (qstore.getCount() - (right + wrong)), "Done!!!", JOptionPane.INFORMATION_MESSAGE);
        try {
            Performance perform = new Performance(UserProfile.getUserid(), exam.getExamID(), right, wrong, (qstore.getCount() - (right + wrong)), (double) (right * 100) / qstore.getCount(), exam.getLaunguae());
            PerformanceDAO.addPerformance(perform);
            JOptionPane.showMessageDialog(null, "Your answers have been saved successfully", "Done!!!", JOptionPane.INFORMATION_MESSAGE);
            PaperSelectionFrame psf = new PaperSelectionFrame();
            psf.setVisible(true);
            this.dispose();
        } catch (SQLException sql) {
            JOptionPane.showMessageDialog(null, "SQLException !" + sql, "Exception!", JOptionPane.ERROR_MESSAGE);
            sql.printStackTrace();
        }

    }//GEN-LAST:event_jButton5ActionPerformed

