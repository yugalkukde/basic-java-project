    private void loadQuestions() {
        try {
            ArrayList<Question> queList = QuestionDAO.getQuestionsByExamId(exam.getExamID());
            for (Question obj : queList) {
                qstore.addQuestion(obj);
            }
        } catch (SQLException sql) {
            JOptionPane.showMessageDialog(null, "Error while connecting to DB! " + sql, "Exception!", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void showQuestion() {
        buttonGroup1.clearSelection();
        question = qstore.getQuestion(pos);
        txtQuestion.setText(question.getQuestion());
        jrOption1.setText(question.getAns1());
        jrOption2.setText(question.getAns2());
        jrOption3.setText(question.getAns3());
        jrOption4.setText(question.getAns4());
        Answer answer = astore.getAnswerByQno(qno);
        if (answer == null) {
            return;
        }
        String chosenAnswer = answer.getChoosenAnswer();
        switch (chosenAnswer) {
            case "Ans1":
                jrOption1.setSelected(true);
                break;
            case "Ans2":
                jrOption2.setSelected(true);
                break;
            case "Ans3":
                jrOption3.setSelected(true);
                break;
            case "Ans4":
                jrOption4.setSelected(true);
                break;
        }
    }

    private String getChosenOp() {
        if (jrOption1.isSelected()) {
            return "Ans1";
        } else if (jrOption2.isSelected()) {
            return "Ans2";
        } else if (jrOption3.isSelected()) {
            return "Ans3";
        } else if (jrOption4.isSelected()) {
            return "Ans4";
        } else {
            return null;
        }
    }
