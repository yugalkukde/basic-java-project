    private int validateInput() {
        if (txtuserID.getText().isEmpty() || txtPassword.getText().isEmpty() || txtRePassword.getText().isEmpty()) {
            return -1;
        } else if (txtuserID.getText().length() < 4 || txtPassword.getText().length() < 4 || txtRePassword.getText().length() < 4) {
            return 0;
        } else if (!txtPassword.getText().equals(txtRePassword.getText())) {
            return 1;
        } else {
            return 2;
        }
    }

    private void update() {
        try {
            boolean result = UserDAO.updateUser(txtuserID.getText(), txtPassword.getText());
            if (result == true) {
                JOptionPane.showMessageDialog(null, "Password updated successfully", "Success!!!", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "UserID not found", "Success!!!", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (SQLException sql) {
            JOptionPane.showMessageDialog(null, "SQLException" + sql, "ERROR!!!", JOptionPane.ERROR_MESSAGE);
        }
    }
