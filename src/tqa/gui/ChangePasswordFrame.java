    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (validateInput() == -1) {
            JOptionPane.showMessageDialog(null, "Plsease fill all fields", "ERROR!!!", JOptionPane.ERROR_MESSAGE);
        } else if (validateInput() == 0) {
            JOptionPane.showMessageDialog(null, "Password must be greater than 4 digits", "ERROR!!!", JOptionPane.ERROR_MESSAGE);
        } else if (validateInput() == 1) {
            JOptionPane.showMessageDialog(null, "Password must be same in both fields", "ERROR!!!", JOptionPane.ERROR_MESSAGE);
        } else {
            update();
            txtPassword.setText("");
            txtRePassword.setText("");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

